#Crie um programa que solicite dois valores numéricos, um operador e uma potência, e realize a exponenciação entre esses dois valores.

valor_um = float(input("Digite um valor numérico (operador): "))
valor_dois = float(input("Digite outro valor numérico: (potência)"))

exponenciacao = valor_um ** valor_dois

print("A exponenciação dos números digitados é: ", exponenciacao)

